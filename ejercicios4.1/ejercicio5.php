<?php

class Calculadora{

    static public function sumar($num1, $num2){

        return $num1 + $num2;

    }
    static public function restar($num1, $num2){

        return $num1 - $num2;

    }
    static public function multiplicar($num1, $num2){

        return $num1 * $num2;

    }
    static public function dividir($num1, $num2){

        return $num1 / $num2;

    }

}